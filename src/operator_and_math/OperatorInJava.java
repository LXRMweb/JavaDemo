/**
 * 讲述Java中基本运算符的用法*/
package operator_and_math;

public class OperatorInJava {
	public static void main(String[] args){
		//"/"除法运算符
		System.out.println("Java中的除法运算符'/':");
		System.out.println("7/2="+7/2);//结果：3
		System.out.println("7f/2="+7f/2);//结果：3.5
		System.out.println("6/5f="+6/5f);//结果1.2
		System.out.println("7/2.0="+7/2.0);//结果：3.5（没有加f/F的浮点数默认是double型的数值，所以2.0默认是double型的
	}

}
